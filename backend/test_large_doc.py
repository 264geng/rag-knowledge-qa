# 计算字数
print(f'文档字数: {len(content)} 字')

# 上传文档
with tempfile.NamedTemporaryFile(mode='w', suffix='.txt', delete=False, encoding='utf-8') as f:
    f.write(content)
    temp_path = f.name

with open(temp_path, 'rb') as f:
    files = {'file': ('公司管理章程.txt', f)}
    r = requests.post(
        f'{BASE_URL}/knowledge-base/{kb_id}/documents',
        files=files,
        headers={'Authorization': f'Bearer {token}'}
    )
os.unlink(temp_path)

print(f'上传状态: {r.status_code}')
if r.status_code == 200:
    print(f'上传成功: {r.json()}')
else:
    print(f'上传失败: {r.text}')

# 设置为公开
r = requests.put(f'{BASE_URL}/interact/knowledge-base/{kb_id}/visibility',
    json={'visibility': 'public'},
    headers={'Authorization': f'Bearer {token}'}
)
print(f'设置公开: {r.status_code}')

# 管理员审核通过
r = requests.put(f'{BASE_URL}/admin/review/knowledge-bases/{kb_id}/approve',
    headers={'Authorization': f'Bearer {token}'}
)
print(f'审核通过: {r.status_code}')

# 等待文档处理完成
import time
print('等待文档处理...')
time.sleep(10)

# 检查文档状态
r = requests.get(f'{BASE_URL}/knowledge-base/{kb_id}/documents',
    headers={'Authorization': f'Bearer {token}'}
)
if r.status_code == 200:
    docs = r.json()
    for doc in docs:
        fname = doc["filename"]
        fstatus = doc["status"]
        fchunk = doc["chunk_count"]
        print(f'文档: {fname} | 状态: {fstatus} | 分块数: {fchunk}')

# 测试问答
print('\n测试大文档问答...')
r = requests.post(f'{BASE_URL}/chat',
    headers={'Authorization': f'Bearer {token}'},
    json={'message': '员工年假有多少天？', 'knowledge_base_id': kb_id},
    timeout=120
)
if r.status_code == 200:
    print(f'问答成功!')
    print(f'回答: {r.json().get("answer", "")[:300]}')
else:
    print(f'问答失败: {r.status_code} - {r.text}')

print('\n测试完成!')
